#include "../include/cube.h"

void	count_nb_line(t_parsing *p)
{
	int		a;

	a = 0;
	while (p->map[a])
	{
		if (p->map[a] == '\n' && (p->map[a + 1] != '\0'
			|| p->map[a - 1] == '1'))
		{
			p->nb_line++;
		}
		a++;
	}
}
